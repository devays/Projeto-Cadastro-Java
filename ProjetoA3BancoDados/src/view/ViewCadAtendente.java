    public void readJTable(){
       DefaultTableModel modelo = (DefaultTableModel) jTableAtendente.getModel();
        modelo.setNumRows(0);
        AtendenteDAO adao = new AtendenteDAO();
    
        for(Atendente a: adao.read()){
            
            modelo.addRow(new Object[]{
                a.getId(),
                a.getNome(),
                a.getSenha()
            });
        
        
        
        }
    
    }

