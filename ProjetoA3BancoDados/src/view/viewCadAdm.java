    public void readJTable(){
        DefaultTableModel modelo = (DefaultTableModel) jTableAdm.getModel();
        modelo.setNumRows(0);
        UsuarioDAO udao = new UsuarioDAO();
        
        for(Usuario u: udao.read()){
            
            modelo.addRow(new Object[]{
                u.getId(),
                u.getLogin(),
                u.getSenha()
            });
        
        }
    
    }
    
    

